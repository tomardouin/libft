#include "libft.h"

char		*ft_strchr(const char *s, int c)
{
	int		i;
	char	*s2;

	s2 = (char*)s;
	i = 0;
	while (s2[i] != c)
	{
		if (s2[i] == '\0')
			return (NULL);
		i++;
	}
	return (&s2[i]);
}
