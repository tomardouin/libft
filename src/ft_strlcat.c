#include "libft.h"

size_t		ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t		i;
	size_t		j;
	size_t		k;
	size_t		l;

	i = 0;
	j = 0;
	k = ft_strlen(dst);
	l = ft_strlen(src);
	if (size <= k)
		return (l + size);
	while (dst[i] && i < size - 1)
		i++;
	while (src[j] && i < size - 1)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (k + l);
}
