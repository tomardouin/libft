#include "libft.h"

char		*ft_strdup(const char *str)
{
	int		i;
	char	*cpy;

	i = 0;
	cpy = (char*)malloc(sizeof(char) * (ft_strlen(str) + 1));
	if (cpy == NULL)
		return (NULL);
	while (str[i])
	{
		cpy[i] = str[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
